def decrypt(buf, key, r9):
    for i in range(len(buf)):
        k = key[i % len(key)]
        pos = i % 3
        if r9 == 1:
            shift = 8 - pos
            if shift == 8:
                val = 0
            else:
                val = (k >> shift) & 0xFF
        else:
            shift = pos
            val = (k << shift) & 0xFF
        buf[i] ^= val
    return buf
